from math import *

print(2.0987)

print(3 + 4.5)

print(5 // 4) # 1 -- neglect the decimals
print(3 ** 3) # 27 -- power of 3
print(9 % 2) # 1 -- remainder

my_num = 5
print(my_num)

print(str(my_num) + " my favorite number") # converts into a string

print(abs(-5)) # non-negative/absolute number

print(max(4, 5, 11, 10)) # get the largest number
print(min(4, 5, 11, 10)) # get the smallest number

print(round(5.2)) # round a number
print(floor(3.7)) # round to backward
print(ceil(3.1)) # round to forward

print(pow(2, 3)) # power of 3
print(sqrt(36)) # get the square root